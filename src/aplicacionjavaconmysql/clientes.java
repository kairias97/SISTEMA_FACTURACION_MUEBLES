    public void updateGrid(String sql){
        conect.CONECTAR();
        try {
            
            String[] titulos = {"ID_CONTACTO", "NOMBRE","TELEFONO","EMAIL"};
            modelo = new DefaultTableModel(null, titulos);
            ResultSet rs = conect.CONSULTAR(sql);
            String[] fila = new String[4];
            while(rs.next()){
                    fila[0] = (String) rs.getString("idcliente");
                    fila[1] = (String) rs.getString("nombre");
                    fila[2] = (String) rs.getString("telefono");  
                    fila[3] = (String) rs.getString("email");
                    modelo.addRow(fila);
            }
            this.tDatos.setModel(modelo);
           
        } catch (Exception e) {
            e.printStackTrace();
        }
        conect.CERRAR();
        
    
        }
