    private void btnModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModActionPerformed
        // TODO add your handling code here:
        if(this.tDatos.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null,"Para modificar primero seleccione un  registro de la lista");
        } else {
            if(this.nombre.getText().equals("") || this.telefono.getText().equals("") || this.email.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Datos a modificar incompletos");
            } else{
                int id = Integer.parseInt(this.idcliente.getText());
                conect.CONECTAR();
                conect.EJECUTAR("UPDATE cliente SET nombre='"+this.nombre.getText()+"', telefono='"+this.telefono.getText()+"', email='"+this.email.getText()+"' WHERE idcliente='"+id+"';");
                conect.CERRAR();
                this.limpiar();
                JOptionPane.showMessageDialog(null,"Datos modificados correctamente!");
                
            }
        }
    }//GEN-LAST:event_btnModActionPerformed

