    public void limpiar(){
        ResultSet resultado = conect.CONSULTAR("SELECT `AUTO_INCREMENT`\n" +
                                                "FROM  INFORMATION_SCHEMA.TABLES\n" +
                                                "WHERE TABLE_SCHEMA = 'agenda'\n" +
                                                "AND   TABLE_NAME   = 'cliente';");
        int ultReg=0;
        try {
            while(resultado.next())
            {
                ultReg=Integer.parseInt(resultado.getString("AUTO_INCREMENT"));
            } 
            String codigo = String.valueOf(ultReg);
            this.idcliente.setText(codigo);
            this.nombre.setText("");
            this.telefono.setText("");
            this.email.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(clientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
